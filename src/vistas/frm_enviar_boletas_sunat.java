    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String anio = jComboBox1.getSelectedItem().toString();
        String mes = jComboBox2.getSelectedItem().toString();
        String sql = "select a.nombre, ds.descripcion, v.fecha, count(*) as nroitems, v.estado "
                + "from ventas as v "
                + "inner join documentos_sunat ds on v.id_tido = ds.id_tido "
                + "inner join almacen a on v.id_almacen = a.id_almacen "
                + "where year(v.fecha) = '" + anio + "' and month(v.fecha) = '" + mes + "' and v.enviado_sunat = 0 and v.serie like 'B%' "
                + "group by v.fecha, v.estado, v.id_tido  "
                + "order by v.fecha asc";
        resumen.verResumenVentas(jTable1, sql);
    }//GEN-LAST:event_jButton1ActionPerformed

