    private void btn_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarActionPerformed

        String fecha = jTable1.getValueAt(fila, 3).toString();
        int tipo = Integer.parseInt(jTable1.getValueAt(fila, 6).toString());
        cl_enviar_resumen envio = new cl_enviar_resumen();
        envio.setFecha(fecha);
        envio.setTipo(tipo);
        if (tipo != 3) {
            JOptionPane.showMessageDialog(null, "Enviado documentos del dia " + fecha + ", espere mensaje de respuesta");
            envio.start();
        }
    }//GEN-LAST:event_btn_agregarActionPerformed

